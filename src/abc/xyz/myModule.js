let ghi="camelCasing" 
function name(ghi){
    let foo=ghi.split("")
    const filteration=foo.filter(a=>{
        if(a===a.toUpperCase()){
            return a
        }
    })
    const two=foo.indexOf(...filteration)
    let four=0
    for (let i=0;i<two;i++){
    four= foo.join('')
    }
    return four
}
